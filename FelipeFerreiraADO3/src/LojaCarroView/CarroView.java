    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if(tblCarro.getRowCount()>0)
        {
            //Verifico se o usuário selecionou alguma linha (Primeira linha = 0)
            if(tblCarro.getSelectedRow()>=0)
            {
                HabilitarFormulario();
                
                //Variável acessória para identifcar se o formulário está em modo de edição ou alteração
                modoTela = "Editar";

                //Atribuo os valores que estão na linha selecionada para a tabela
                lblID.setText(tblCarro.getModel().getValueAt(tblCarro.getSelectedRow(), 0).toString());
                txtAno.setText(tblCarro.getModel().getValueAt(tblCarro.getSelectedRow(), 1).toString());
                txtModelo.setText(tblCarro.getModel().getValueAt(tblCarro.getSelectedRow(), 2).toString());
                txtValor.setText(tblCarro.getModel().getValueAt(tblCarro.getSelectedRow(), 3).toString());
            }
            else
            {
                JOptionPane.showMessageDialog(this,"Selecione um carro para editar!");
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this,"Não há carros para editar!");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
       
        if(tblCarro.getRowCount()>0)
        {
            //Resgato o número da linha pelo JTable
            int numeroLinha = tblCarro.getSelectedRow();
            
           
            int ID= Integer.parseInt(tblCarro.getModel().getValueAt(numeroLinha, 0).toString());
            
           
            if(CarroController.excluir( ID ))
            {
                this.LoadTable();
                JOptionPane.showMessageDialog(this,"Carro excluído da base de dados");
            }else{
                JOptionPane.showMessageDialog(this,"Falha ao excluir o carro!");
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this,"Não há carros para excluir!");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private boolean ValidarFormulario() {
        
        if(this.txtAno.getText().equalsIgnoreCase(""))
        {
            JOptionPane.showMessageDialog(this,"Defina o ano do carro");
            return false;
        }
        
        if(this.txtModelo.getText().equalsIgnoreCase(""))
        {
            JOptionPane.showMessageDialog(this,"Defina o modelo do carro");
            return false;
        }
        if(this.txtValor.getText().equalsIgnoreCase(""))
        {
            JOptionPane.showMessageDialog(this, "Defina o valor do carro");
            return false;
        }
        
        return true;
        
    }
    
    
    
    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
         if(ValidarFormulario())
        {
            if(modoTela.equals("Criar"))
            {
               
                if(CarroController.salvar(txtModelo.getText(), Integer.parseInt(txtAno.getText()),Double.parseDouble(txtValor.getText())))
                {
                    
                    this.LoadTable();
                    
                    JOptionPane.showMessageDialog(null,"Carro cadastrado com sucesso!");
                    LimparFormulario();
                }else{
                    JOptionPane.showMessageDialog(null,"Falha ao cadastrar carro!");
                }

            }else
            {

               
                if(CarroController.atualizar(Integer.parseInt(lblID.getText()),Integer.parseInt(txtAno.getText()),Double.parseDouble(txtValor.getText()), txtModelo.getText()))
                {
                    
                    this.LoadTable();
                    JOptionPane.showMessageDialog(this,"Carro atualizado com sucesso!");
                }else{
                    JOptionPane.showMessageDialog(this,"Falha ao atualizar carro!");
                }

            }

            LimparFormulario();
            DesabilitarFormulario();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

