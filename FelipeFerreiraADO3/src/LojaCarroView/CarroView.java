    public void LoadTable(){
        
     
        ArrayList<String[]> linhasCarros = CarroController.consultar();
        
        
        DefaultTableModel tmCarro = new DefaultTableModel();
        tmCarro.addColumn("ID");
        tmCarro.addColumn("Ano");
        tmCarro.addColumn("Valor");
        tmCarro.addColumn("Modelo");
        tblCarro.setModel(tmCarro);
        
       
        tblCarro.removeColumn(tblCarro.getColumnModel().getColumn(0));
        
      
        for(String[] c:linhasCarros)
        {
            tmCarro.addRow(c);
        }
        
       
        tblCarro.getColumnModel().getColumn(0).setPreferredWidth(300);
        tblCarro.getColumnModel().getColumn(1).setPreferredWidth(100);
    }
    
     public void DesabilitarFormulario()
    {
        txtAno.setEditable(false);
        txtModelo.setEditable(false);
        txtValor.setEditable(false);
        
        btnSalvar.setEnabled(false);
        btnCancelar.setEnabled(false);
    }
     
     public void HabilitarFormulario()
    {
        txtAno.setEditable(true);
        txtModelo.setEditable(true);
        txtValor.setEditable(true);
        
        btnSalvar.setEnabled(true);
        btnCancelar.setEnabled(true);
    }
     
     public void LimparFormulario()
    {
        txtAno.setText("");
        txtModelo.setText("");
        txtValor.setText("");
        
    }

